#include "3-calc.h"
/**
 * op_add - addition
 * @a: num 1
 * @b: num 2
 * Return: result of operation
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction
 * @a: num 1
 * @b: num 2
 * Return: result of operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: num 1
 * @b: num 2
 * Return: result of operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: num 1
 * @b: num 2
 * Return: result of operation
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo
 * @a: num 1
 * @b: num 2
 * Return: result of operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
